import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from tensorflow.keras.preprocessing.text import Tokenizer
from tensorflow.keras.preprocessing.sequence import pad_sequences
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Embedding, LSTM, Dense, Dropout
from sklearn.model_selection import train_test_split
from sklearn.metrics import classification_report, confusion_matrix, accuracy_score
import time

# 1. LOAD DATA
df = pd.read_csv('dataset_gojek_biner_bersih.csv')
X = df['clean_text'].astype(str).values
y = df['label_encoded'].values

# Tokenization
max_words, max_len = 5000, 50
tokenizer = Tokenizer(num_words=max_words)
tokenizer.fit_on_texts(X)
X_pad = pad_sequences(tokenizer.texts_to_sequences(X), maxlen=max_len)
X_train, X_test, y_train, y_test = train_test_split(X_pad, y, test_size=0.2, random_state=42)

# 2. KONFIGURASI 5 SKENARIO
skenarios = [
    {"epoch": 10, "dropout": 0.2, "unit": 64, "batch": 32, "rdropout": 0.2},
    {"epoch": 20, "dropout": 0.2, "unit": 64, "batch": 32, "rdropout": 0.2},
    {"epoch": 30, "dropout": 0.2, "unit": 64, "batch": 32, "rdropout": 0.2},
    {"epoch": 50, "dropout": 0.5, "unit": 64, "batch": 32, "rdropout": 0.2},
    {"epoch": 20, "dropout": 0.2, "unit": 128, "batch": 32, "rdropout": 0.2},
]

all_results = []

# 3. PROSES TRAINING OTOMATIS
for i, config in enumerate(skenarios):
    print(f"\n{'='*40}")
    print(f"RUNNING SKENARIO {i+1} / 5")
    print(f"Konfigurasi: {config}")
    print(f"{'='*40}")

    model = Sequential([
        Embedding(max_words, 128, input_length=max_len),
        LSTM(config['unit'], dropout=config['dropout'], recurrent_dropout=config['rdropout']),
        Dense(1, activation='sigmoid')
    ])
    model.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])

    # Training
    history = model.fit(X_train, y_train, epochs=config['epoch'], batch_size=config['batch'],
                        validation_data=(X_test, y_test), verbose=1)

    # Prediksi & Evaluasi
    y_pred = (model.predict(X_test) > 0.5).astype("int32")
    report = classification_report(y_test, y_pred, output_dict=True)
    acc = accuracy_score(y_test, y_pred)

    # Simpan hasil untuk grafik akhir
    all_results.append({
        "skenario": i+1,
        "acc": acc,
        "f1": report['macro avg']['f1-score'],
        "prec": report['weighted avg']['precision'],
        "rec": report['weighted avg']['recall'],
        "config": f"S{i+1}"
    })

    # Output Visual
    fig, ax = plt.subplots(1, 3, figsize=(20, 5))

    # Plot Accuracy (Warna Biru & Orange Standar)
    ax[0].plot(history.history['accuracy'], label='Train Acc')
    ax[0].plot(history.history['val_accuracy'], label='Val Acc')
    ax[0].set_title(f'S{i+1}: Model Accuracy')
    ax[0].legend()

    # Plot Loss
    ax[1].plot(history.history['loss'], label='Train Loss')
    ax[1].plot(history.history['val_loss'], label='Val Loss')
    ax[1].set_title(f'S{i+1}: Model Loss')
    ax[1].legend()

    # Plot Confusion Matrix
    sns.heatmap(confusion_matrix(y_test, y_pred), annot=True, fmt='d', cmap='Blues', ax=ax[2])
    ax[2].set_title(f'S{i+1}: Confusion Matrix')


    plt.tight_layout()
    plt.show()

    # TABEL DETAIL
    print(f"\n HASIL EVALUASI SKENARIO {i+1}")
    print("-" * 65)
    print(f"{'Kelas':<15} | {'Precision':<10} | {'Recall':<10} | {'F1-Score':<10}")
    print("-" * 65)
    for l, n in [('0', 'Negative'), ('1', 'Positive')]:
        print(f"{n:<15} | {report[l]['precision']:.4f}     | {report[l]['recall']:.4f}   | {report[l]['f1-score']:.4f}")
    print("-" * 65)
    print(f"{'Macro Avg':<15} | {report['macro avg']['precision']:.4f}     | {report['macro avg']['recall']:.4f}   | {report['macro avg']['f1-score']:.4f}")
    print(f"{'Weighted Avg':<15} | {report['weighted avg']['precision']:.4f}     | {report['weighted avg']['recall']:.4f}   | {report['weighted avg']['f1-score']:.4f}")
    print("-" * 65)
    print(f"TOTAL ACCURACY : {acc:.4f}\n")

# 4. GRAFIK EVALUASI KOMPARASI AKHIR
print("\n Menghasilkan Grafik Perbandingan Semua Skenario...")
summary_df = pd.DataFrame(all_results)

plt.figure(figsize=(12, 6))
plt.plot(summary_df['config'], summary_df['acc'], marker='o', label='Accuracy', linewidth=2)
plt.plot(summary_df['config'], summary_df['f1'], marker='s', label='F1-Score (Macro)', linestyle='--')
plt.plot(summary_df['config'], summary_df['prec'], marker='^', label='Precision (Weighted)', linestyle=':')



plt.title('Perbandingan Performa Model antar Skenario', fontsize=14)
plt.ylabel('Score')
plt.xlabel('Skenario')
plt.grid(True, alpha=0.3)
plt.legend()
plt.show()

