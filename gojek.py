df['Ulasan'] = df['Ulasan'].astype(str)




# 2. FUNGSI PRE-PROCESSING


# A. Case Folding
def case_folding(text):
    return text.lower()

# B. Cleaning Text
def cleaning_text(text):
    text = re.sub(r'http\S+', '', text) # Hapus URL
    text = re.sub(r'(@\w+|#\w+)', '', text) # Hapus Mention dan Hashtag
    text = re.sub(r'[^a-zA-Z\s]', ' ', text) # Hapus angka dan tanda baca
    text = re.sub(r'\s+', ' ', text).strip() # Hapus spasi berlebih
    return text

# C. Normalization (Kamus Gaul Gojek)
norm_dict = {
    'apk': 'aplikasi', 'app': 'aplikasi', 'bgt': 'banget', 'bgtt': 'banget',
    'tdk': 'tidak', 'gak': 'tidak', 'gk': 'tidak', 'ga': 'tidak', 'enggak': 'tidak',
    'sy': 'saya', 'jg': 'juga', 'jd': 'jadi', 'krn': 'karena', 'kalo': 'kalau',
    'dgn': 'dengan', 'yg': 'yang', 'udah': 'sudah', 'udh': 'sudah', 'blm': 'belum',
    'driver': 'pengemudi', 'ojol': 'ojek online', 'gopay': 'saldo', 'min': 'admin',
    'nyasar': 'tersesat', 'lemot': 'lambat', 'lola': 'lambat', 'error': 'rusak',
    'update': 'perbarui', 'cpt': 'cepat', 'bgs': 'bagus', 'dr': 'dari', 'tp': 'tapi'
}

def normalization(text):
    words = text.split()
    normalized_words = [norm_dict.get(word, word) for word in words]
    return ' '.join(normalized_words)

# D. Stopword Removal
factory_stopword = StopWordRemoverFactory()
stopwords = factory_stopword.get_stop_words()
words_to_keep = ['tidak', 'kurang', 'bukan', 'jangan', 'belum']
custom_stopwords = [word for word in stopwords if word not in words_to_keep]

def remove_stopword(text):
    words = text.split()
    filtered_words = [word for word in words if word not in custom_stopwords]
    return ' '.join(filtered_words)

# E. Stemming
factory_stemmer = StemmerFactory()
stemmer = factory_stemmer.create_stemmer()

def stemming(text):
    return stemmer.stem(text)



# 3. EKSEKUSI PRE-PROCESSING

print("🔄 1/5 Memproses Case Folding...")
df['clean_text'] = df['Ulasan'].apply(case_folding)

print("🔄 2/5 Memproses Cleaning Text...")
df['clean_text'] = df['clean_text'].apply(cleaning_text)

print("🔄 3/5 Memproses Normalization...")
df['clean_text'] = df['clean_text'].apply(normalization)

print("🔄 4/5 Memproses Stopword Removal...")
df['clean_text'] = df['clean_text'].apply(remove_stopword)

print("🔄 5/5 Memproses Stemming (Estimasi 45-60 Menit. Silakan ditinggal ngopi!)...")
# progress_apply untuk melihat loading bar
df['clean_text'] = df['clean_text'].progress_apply(stemming)

# Hapus baris kosong akibat proses cleaning
df = df[df['clean_text'].str.strip() != '']
